function [v11, v12, v13, v21, v22, v23, v31, v32, v33] = MSE(Z11, Z12, Z13, Z21, Z22, Z23, Z31, Z32, Z33, g11, g12, g13, g21, g22, g23, g31, g32, g33, n0)
%update filters by MSE criterion

%MSE
R1 = (Z11*g11+Z12*g21+Z13*g31)*(Z11*g11+Z12*g21+Z13*g31)'...
    +(Z11*g12+Z12*g22+Z13*g32)*(Z11*g12+Z12*g22+Z13*g32)'...
    +(Z11*g13+Z12*g23+Z13*g33)*(Z11*g13+Z12*g23+Z13*g33)'...
    +n0*eye(2);

R2 = (Z21*g11+Z22*g21+Z23*g31)*(Z21*g11+Z22*g21+Z23*g31)'...
    +(Z21*g12+Z22*g22+Z23*g32)*(Z21*g12+Z22*g22+Z23*g32)'...
    +(Z21*g13+Z22*g23+Z23*g33)*(Z21*g13+Z22*g23+Z23*g33)'...
    +n0*eye(2);

R3 = (Z31*g11+Z32*g21+Z33*g31)*(Z31*g11+Z32*g21+Z33*g31)'...
    +(Z31*g12+Z32*g22+Z33*g32)*(Z31*g12+Z32*g22+Z33*g32)'...
    +(Z31*g13+Z32*g23+Z33*g33)*(Z31*g13+Z32*g23+Z33*g33)'...
    +n0*eye(2);

P11 = Z11*g11+Z12*g21+Z13*g31;
P12 = Z11*g12+Z12*g22+Z13*g32;
P13 = Z11*g13+Z12*g23+Z13*g33;

P21 = Z21*g11+Z22*g21+Z23*g31;
P22 = Z21*g12+Z22*g22+Z23*g32;
P23 = Z21*g13+Z22*g23+Z23*g33;

P31 = Z31*g11+Z32*g21+Z33*g31;
P32 = Z31*g12+Z32*g22+Z33*g32;
P33 = Z31*g13+Z32*g23+Z33*g33;

v11 = R1\P11;
v12 = R1\P12;
v13 = R1\P13;

v21 = R2\P21;
v22 = R2\P22;
v23 = R2\P23;

v31 = R3\P31;
v32 = R3\P32;
v33 = R3\P33;

%Normalize

M1 = sqrt(norm(v11)^2+norm(v12)^2+norm(v13)^2);
v11 = v11/M1;
v12 = v12/M1;
v13 = v13/M1;

M2 = sqrt(norm(v21)^2+norm(v22)^2+norm(v23)^2);
v21 = v21/M2;
v22 = v22/M2;
v23 = v23/M2;

M3 = sqrt(norm(v31)^2+norm(v32)^2+norm(v33)^2);
v31 = v31/M3;
v32 = v32/M3;
v33 = v33/M3;
end

    
    